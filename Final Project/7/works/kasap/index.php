INFO: Every country has their speciality. Belgium has chocolate, Germany has beer, Italy has the pizza, America has the hamburger and Turkey has the doner.

Doner is one of the most popular fast foods in the world but unlike pizza or burger, there has never been a doner brand. Until KasapDoner.

The good news was, there was no other doner brand to compete with. But like the rest of the world, the Turkish fast food market is dominated by burger brands.

A “burger” is not just food, it represents American culture. Turkish people are proud of their country and we wanted to remind Turkish people that they had a better option. Their very own culture and their own traditional fast food.

The most symbolic feature of doner is used as the hero of the campaign: The doner knife. In each film, we see the knife cut something that represents American culture into two.

Even with the limited budget, the campaign was a huge success. KasapDoner became famous overnight and has become Turkey's fastest growing fast food chain.
